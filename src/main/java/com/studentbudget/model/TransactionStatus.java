package com.studentbudget.model;

/**
 * Перечисление статусов транзакции.
 * Определяет возможные состояния транзакции в системе.
 */
public enum TransactionStatus {
    /**
     * Активная транзакция, учитывается в расчетах баланса
     */
    ACTIVE,
    
    /**
     * Отмененная транзакция, не учитывается в расчетах
     */
    CANCELLED,
    
    /**
     * Ожидающая транзакция, временно не учитывается в расчетах
     */
    PENDING,
    
    /**
     * Отклоненная транзакция, не учитывается в расчетах
     */
    REJECTED
}